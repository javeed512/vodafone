package calsi;

import static java.lang.System.*;

public class CalculatorService {

	public int add(int a, int b) {
		
		//out.println("add method");

		return a + b;

	}

	public int sub(int a, int b) {

		return a - b;

	}

	public int mul(int a, int b) {

		return a * b;

	}

	public int div(int a, int b) {

		return a / b;

	}

}
